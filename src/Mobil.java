    private void loadTabel() {
        String namaKolom[] = {"id_mobil","merek","tipe","tahun","nopol","harga","status"};
        rs=con.querySelect(namaKolom,"tb_mobil");
        table_mobil.setModel(new SetTable(rs));
    }

    private void create() {
      
        try {
 
            if( !txt_merk.getText().isEmpty() && !txt_tipe.getText().isEmpty() && !txt_nopolisi.getText().isEmpty() && !txt_harga.getText().isEmpty()){
                String kolom[] = {"merek","tipe","tahun","nopol","harga","status"};
                String isi[] = { txt_merk.getText(),txt_tipe.getText(),cb_tahun.getSelectedItem().toString(),txt_nopolisi.getText(),txt_harga.getText(),cb_status.getSelectedItem().toString()};
                
                System.out.println( con.queryInsert("tb_mobil",kolom,isi));
               
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
            }else{
                JOptionPane.showMessageDialog(this, "Inputan tidak boleh kosong");
            } 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Input data gagal");
             System.out.println("salah");
        }
        loadTabel();
        clear();
    }

