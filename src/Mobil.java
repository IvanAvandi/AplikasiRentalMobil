    private void btn_ubah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubah1ActionPerformed
        try {
            if( !txt_merk.getText().isEmpty() && !txt_tipe.getText().isEmpty() && !txt_nopolisi.getText().isEmpty() && !txt_harga.getText().isEmpty()){
                String kolom[] = {"merek","tipe","tahun","nopol","harga","status"};
                String isi[] = { txt_merk.getText(),txt_tipe.getText(),cb_tahun.getSelectedItem().toString(),txt_nopolisi.getText(),txt_harga.getText(),cb_status.getSelectedItem().toString()};

                // Melakukan update dan mengecek hasilnya
                String hasilUpdate = con.queryUpdate("tb_mobil", kolom, isi, "id_mobil='" + String.valueOf(table_mobil.getValueAt(table_mobil.getSelectedRow(), 0)) + "'");

                if (hasilUpdate.isEmpty()) {
                    // Jika tidak ada pesan error, artinya update berhasil
                    JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                    loadTabel();  // Memuat ulang tabel dengan data terbaru
                } else {
                    // Jika ada error, tampilkan pesan error
                    JOptionPane.showMessageDialog(this, "Gagal mengubah data: " + hasilUpdate);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Inputan tidak boleh kosong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengubah data");
            e.printStackTrace();  // Menampilkan detail kesalahan jika ada
        }
        clear();   // Membersihkan input form setelah proses
    }//GEN-LAST:event_btn_ubah1ActionPerformed

