    private void btn_cari1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cari1ActionPerformed
        if(txt_cari.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Input data pencarian");
        }else{
            try {
                rs=con.querySelectAll("tb_mobil", "id_mobil LIKE '%"+txt_cari.getText()+"%' OR merek LIKE '%"+txt_cari.getText()+"%' OR tipe LIKE '%"+txt_cari.getText()+"%' OR nopol LIKE '%"+txt_cari.getText()+"%' OR harga LIKE '%"+txt_cari.getText()+"%' OR tahun LIKE '%"+txt_cari.getText()+"%'" );
                table_mobil.setModel(new Database.SetTable(rs));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Pencarian gagal");
            }
        }
        clear();
    }//GEN-LAST:event_btn_cari1ActionPerformed

