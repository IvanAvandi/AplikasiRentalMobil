    private void btn_hapus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapus1ActionPerformed
        try {
            String id=String.valueOf(table_mobil.getValueAt(table_mobil.getSelectedRow(),0));
            if (JOptionPane.showConfirmDialog(this, "Yakin menghapus data", "Peringatan", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                con.queryDelete("tb_mobil","id_mobil="+id);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
            }else{
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data belum dipilih");
        }
        loadTabel();
        clear();
    }//GEN-LAST:event_btn_hapus1ActionPerformed

